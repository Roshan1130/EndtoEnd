package com.endtoend.dtos;

public class MatchEventsDto {

	private Integer matchEventsId;
	private Integer eventId;
	private String description;
	public Integer getMatchEventsId() {
		return matchEventsId;
	}
	public void setMatchEventsId(Integer matchEventsId) {
		this.matchEventsId = matchEventsId;
	}
	public Integer getEventId() {
		return eventId;
	}
	public void setEventId(Integer eventId) {
		this.eventId = eventId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
}
